// define our first variable
// when someone clicks on acorn icon, we want the background to turn red

let acornIcon = document.querySelector(".acorn");

// what happens when someone interacts with the acorn
function turnRed() {
    document.body.style.background = "rgb(224,64,26)";

}

//add an event listener
// when someone clicks on the red circle, change the background red

acornIcon.addEventListener("mouseover", turnRed);



